package com.projectapp.lessons._11_Lambda.A9_ReferenceOnMethods;


public class A9_ReferenceOnStaticMethods {
    static String myUpper (String str){

        String newStr = "";
        for (int i = 0; i < 1; i++) {
            newStr += str.toUpperCase();
        }
        return newStr;
    }


}
